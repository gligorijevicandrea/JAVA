package class7;

public class ForLoops5 {
    public static void main(String[] args) {

        // Do table of 5

        for (int i = 1; i <=10 ; i++) {
            System.out.println("5 * "+i+" = "+(5*i));
        }

       // Do table of 3

        for (int i = 1; i <=10; i++) {
            System.out.println("3 * "+i+" = "+(3*i));
        }







    }
}
