package class3;

public class OperatorDemo_3 {
    public static void main(String[] args) {
        int number=10;
        System.out.println(number/3); //3
        System.out.println(number%3); //1

        System.out.println(16%4); // nothing left

        System.out.println(16/5);
        System.out.println(16%5); // left 1 piece

        System.out.println(156%2);
        System.out.println(155%2); // if answer is 0 , it's even number , we use 2 for Even and Or

        System.out.println(1555%5); // if the answer is 0 , deljiv je broj skroz

    }
}
