package class3;

public class RelationalOperators_6 {
    public static void main(String[] args) {

        int number=10;
        int number2=10;
        System.out.println(number+number2);

        /*
        Arithmetic operators
        int+int=int
        int/int=> int
        int -int=int
         */

        /*
        Relational operators
        > greater than
        < less than
        >=  greater or equal
        <= less or equal
        !=  means not equal
        ==  checking if 2 things are equal
         */

        boolean result=20>10;
        System.out.println(result);

        System.out.println(20>10); // 20 greater than 10 => true
        System.out.println(5>10); //is 5 greater than 10 =>false
        System.out.println(5!=10); // is 5 not equal to 10 => true
        System.out.println(5==10); // is 5 equal to 10 => false
        System.out.println(5==5); // is 5 equal to 5 => true
        System.out.println(10>=20); // is 10 greater or equal to 20  => false
        System.out.println(10<=20); // is 10 less or equal to 20 => true
        System.out.println(10<=10); // is 10 less or equal to 10 => true





    }
}
