package class2;

public class OperatorsDemo {
    public static void main(String[] args) {
        int number1=10;
        int number2=20;
        System.out.println(number1+number2);
        System.out.println(number1-number2);
        System.out.println(number1*number2);
        System.out.println(number1/number2);

        float number3=10f;
        float number4=20f;
        System.out.println(number3/number4);

    }
}
