package class21;

public class Calculator {


    void add(int num1,int num2){
        System.out.println(num1+num2);
    }

    void add(int num1,int num2,int num3){  // adding number of parameters , num3
        System.out.println(num1+num2+num3);
    }

    void add(int num1,double num2){  // changing int to double (data type)
        System.out.println(num1+num2);
    }

    void add(double num2,int num1){  // changing sequence , num2 then num1
        System.out.println(num1+num2);
    }



}
