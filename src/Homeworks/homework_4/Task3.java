public class Task3 {
    public static void main(String[] args) {

        // Print numbers from 100 to 1

        int number=100;

        while (number>=1){
            System.out.println(number);
            number--;
        }

        System.out.println("******************");

        for (int i = 100; i >=1; i--) {
            System.out.println(i);

        }


    }
}
